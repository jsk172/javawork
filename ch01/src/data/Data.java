package data;

public class Data {
	public static void main(String[] args) {
		/*
		 * int, long의 사용
		 * int형 크기 - 4byte(32bit)
		 * long형 - 8byte(64bit)
		 */
		int num1 = 1234567890; // 0 하나 더 붙이면 범위밖
		long num2 = 12345678900L; //long형은 숫자뒤에 대문자 'L' 이나 소문자 'l' 을 붙여햐 함.
		
		System.out.println(num1);
		System.out.println(num2);
		
	}
}
